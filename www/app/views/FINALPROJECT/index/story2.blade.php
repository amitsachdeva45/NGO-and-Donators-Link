As we walk into the Upper Primary school in Pati (Patancheru,Hyderabad), an Light The Future mid-day meal van enters the school. A few boys run towards the driver as he unloads the hot containers of food. It&rsquo;s a Thursday and all are excited about the weekly special: Biryani. A stern voice bellows, &rdquo;Get back to class.&rdquo; The boys instantly acknowledge their mistake and with shy smiles they run back to class. Mr. Ramulu, the school headmaster welcomes us to his school as he briefs us about his school.    
We are then introduced to R. Soujanya, an 8th standard student at the school. She is a topper in her class. Both her parents are agricultural workers who work and together they earn about Rs.150-200 per day.We do find it hard at times but we are satisfied,&rdquo; says Soujanya who puts on a stern look. She has a younger brother, who studies at a school close to their house. Her parents put him another school due to financial constraints.  
